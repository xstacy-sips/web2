export const MENU_DATA_STORAGE_KEY = 'xstacy.menuData.v1';

export const getDefaultMenuData = () => JSON.parse(JSON.stringify(menuData));

export const loadMenuData = () => {
  if (typeof window === 'undefined') {
    return getDefaultMenuData();
  }

  try {
    const raw = window.localStorage.getItem(MENU_DATA_STORAGE_KEY);
    if (!raw) return getDefaultMenuData();
    return JSON.parse(raw);
  } catch (error) {
    return getDefaultMenuData();
  }
};

export const saveMenuData = (data) => {
  if (typeof window === 'undefined') return;
  try {
    window.localStorage.setItem(MENU_DATA_STORAGE_KEY, JSON.stringify(data));
  } catch (error) {
    // Ignore storage errors (private mode, quota, etc.)
  }
};

export const clearMenuData = () => {
  if (typeof window === 'undefined') return;
  try {
    window.localStorage.removeItem(MENU_DATA_STORAGE_KEY);
  } catch (error) {
    // Ignore storage errors
  }
};

